const core = require("@actions/core");
const github = require("@actions/github");
const { fullAssistantProcessor } = require("./utils/fullAssistantProcessor");
const { config } = require("./utils/config");

async function run() {
    try {
        const token = core.getInput("github-token", { required: true });
        const commitMessage = core.getInput("commit-message", {
            required: true,
        });

        if (!config.implementationFile) {
            throw new Error("No failed test implementation file found to update");
        }

        // Process with the assistant
        const newData = await fullAssistantProcessor();

        if (!newData) {
            throw new Error(
                "No new data was returned from the assistant processor"
            );
        }

        const octokit = github.getOctokit(token);
        const { owner, repo } = github.context.repo;
        const newBranchName = `ai-fix-${github.context.payload.pull_request.number}`;

        const filePath = config.implementationFile;

        // Create or update file in the new branch
        await octokit.rest.repos.createOrUpdateFileContents({
            owner,
            repo,
            path: filePath,
            message: commitMessage,
            content: Buffer.from(newData).toString("base64"),
            branch: newBranchName
        });

        // Create pull request
        await octokit.rest.pulls.create({
            owner,
            repo,
            title: "AI-assisted Fix for Failed Tests",
            head: newBranchName,
            base: github.context.payload.pull_request.base.ref,
            body: `Automated fix generated by AI for failed tests in PR #${github.context.payload.pull_request.number}`
        });

        console.log(
            `File ${filePath} has been updated in branch ${newBranchName} successfully with assistant results.`
        );
    } catch (error) {
        core.setFailed(error.message);
    }
}

run();